using System;
namespace Cabinink.Writer.Cores
{
   /// <summary>
   /// 当前层级的写作时间段的接口
   /// </summary>
   public interface ILevelCreationTime
   {
      /// <summary>
      /// 获取或设置当前层级的创作时间
      /// </summary>
      DateTime CreationTime { get; set; }
      /// <summary>
      /// 获取或设置当前层级的完结时间
      /// </summary>
      DateTime BreakupTime { get; set; }
   }
}
